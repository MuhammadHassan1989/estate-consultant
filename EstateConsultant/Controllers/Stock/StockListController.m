- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"PositionCell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier] autorelease];
    }
    
    Position *position = [self.positions objectAtIndex:indexPath.row];
    Layout *layout = ((House *)[position.houses anyObject]).layout;
    cell.textLabel.text = [NSString stringWithFormat:@"%@ - %@", position.name, layout.name];
    
    NSPredicate *predicate = [NSPredicate predicateWithFormat:@"SELF.status == 1"];
    NSSet *stockHouses = [position.houses filteredSetUsingPredicate:predicate];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%i套待售", stockHouses.count];
    
    return cell;
}

